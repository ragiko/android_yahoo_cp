package com.parse.mealspotting;

public class Chat {
    private String userName_;
    private String message_;

    public void setUserName(String text) {
    	userName_ = text;
    }

    public String getUserName() {
        return userName_;
    }

    public void setMessage(String text) {
    	message_ = text;
    }

    public String getMessage() {
        return message_;
    }
}
